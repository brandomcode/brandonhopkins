function formatTime(s) {
  const m = Math.floor(s / 60);
  const sec = Math.floor(s % 60);
  return `${m}:${sec.toString().padStart(2, '0')}`;
}

function buildAudioPlayer(file, id) {
  return `
    <div class="audio-player" id="player-${id}">
      <audio id="audio-${id}" src="${file}" preload="metadata"></audio>
      <div class="audio-controls">
        <button class="audio-play-btn" id="playbtn-${id}" onclick="togglePlay('${id}')">Play</button>
        <div class="audio-progress-wrap" id="progresswrap-${id}" onclick="seekAudio(event, '${id}')">
          <div class="audio-progress-bar" id="progressbar-${id}"></div>
        </div>
        <span class="audio-time" id="audiotime-${id}">0:00</span>
      </div>
    </div>
  `;
}

function togglePlay(id) {
  const audio = document.getElementById(`audio-${id}`);
  const btn = document.getElementById(`playbtn-${id}`);
  if (!audio) return;
  if (currentAudio && currentAudio !== audio) {
    currentAudio.pause();
    const otherId = currentAudio.id.replace('audio-', '');
    const otherBtn = document.getElementById(`playbtn-${otherId}`);
    if (otherBtn) otherBtn.textContent = 'Play';
  }
  if (audio.paused) {
    audio.play();
    btn.textContent = 'Pause';
    currentAudio = audio;
  } else {
    audio.pause();
    btn.textContent = 'Play';
  }
  audio.ontimeupdate = () => {
    const bar = document.getElementById(`progressbar-${id}`);
    const time = document.getElementById(`audiotime-${id}`);
    if (bar && audio.duration) bar.style.width = `${(audio.currentTime / audio.duration) * 100}%`;
    if (time) time.textContent = formatTime(audio.currentTime);
  };
  audio.onended = () => {
    btn.textContent = 'Play';
    const bar = document.getElementById(`progressbar-${id}`);
    if (bar) bar.style.width = '0%';
    const time = document.getElementById(`audiotime-${id}`);
    if (time) time.textContent = '0:00';
  };
}

function seekAudio(e, id) {
  const audio = document.getElementById(`audio-${id}`);
  const wrap = document.getElementById(`progresswrap-${id}`);
  if (!audio || !audio.duration) return;
  const rect = wrap.getBoundingClientRect();
  audio.currentTime = ((e.clientX - rect.left) / rect.width) * audio.duration;
}

